                sb.AppendLine("//*      1   = valid input, value was stored                                     *");
                sb.AppendLine("//*     -1   = invalid input, value was rejected                                 *");
                sb.AppendLine("//*                                                                              *");
                sb.AppendLine("//*  Example:                                                                    *");
                sb.AppendLine("//*      INT FUNCTION Verify_rec_RegisterX(RName, PhaseNum, tag, value)       *");
                sb.AppendLine("//*          IF StrTrim(value) = \"\" THEN                                         *");
                sb.AppendLine("//*              RETURN -1; // Invalid: empty input                              *");
                sb.AppendLine("//*          ELSE                                                                *");
                sb.AppendLine("//*              Recipe_UpsertTagValue(RName, tag, value,                        *");
                sb.AppendLine("//*                  \"<Description>\",                                           *");
                sb.AppendLine("//*                  IntToStr(PhaseNum + 1), 1);                                 *");
                sb.AppendLine("//*              RETURN 1; // Valid and saved                                   *");
                sb.AppendLine("//*          END                                                                 *");
                sb.AppendLine("//*      END                                                                     *");
                sb.AppendLine("//*                                                                              *");
                sb.AppendLine("//*  Do NOT remove or rename any function.                                       *");
                sb.AppendLine("//*  Only edit the logic inside each function body.                              *");
                sb.AppendLine("//********************************************************************************");


                sb.AppendLine();

                sb.AppendLine();
            }

            foreach (var fieldName in distinctFields)
            {
                if (!existingFunctions.Contains(fieldName))
                {
                    sb.AppendLine($"\r\nINT FUNCTION Verify_{fieldName}(STRING tag,INT PhaseNum,STRING value,STRING tagType,STRING recName)");
                    sb.AppendLine("    IF StrTrim(value) = \"\" THEN ");
                    sb.AppendLine("     RETURN - 1; //ERROR");
                    sb.AppendLine("    END");
                    sb.AppendLine("     Recipe_UpsertTagValue(tagType,recName, tag, value, \"<Description>\", IntToStr(PhaseNum + 1), 1);");
                    sb.AppendLine("     RETURN 1; //OK ");
                    sb.AppendLine("END");
                    sb.AppendLine();

                }
            }


            if (sb.Length > 0)
            {
                try
                {
                    if (isNewFile)
                    {
                        File.WriteAllText(functionsFilePath, sb.ToString());
                    }
                    else
                    {
                        File.AppendAllText(functionsFilePath, sb.ToString());
                    }
                }
                catch (IOException ioEx)
                {
                    Console.WriteLine($"I/O error while writing to file: {ioEx.Message}");
                }
                catch (UnauthorizedAccessException uaEx)
                {
                    Console.WriteLine($"Access denied when writing to file: {uaEx.Message}");
                }
                catch (Exception ex)
                {
                    Console.WriteLine($"Unexpected error while appending functions: {ex.Message}");
                }
            }
        }



