        public static void GenerateRecipeFunction(IEnumerable<string> fieldNames, string outputFilePath)
        {
            if (fieldNames == null || !fieldNames.Any())
                throw new ArgumentException("Field names are required", nameof(fieldNames));

            var distinctFields = fieldNames.Distinct(StringComparer.OrdinalIgnoreCase).ToList();

            var sb = new StringBuilder();

            sb.AppendLine("//***************************************");
            sb.AppendLine("//***  Warning: Auto-generated file   ***");
            sb.AppendLine("//***  Do NOT edit this file manually ***");
            sb.AppendLine("//***************************************");
            sb.AppendLine();




            sb.AppendLine("STRING FUNCTION GetValidVal(STRING Field)");
            sb.AppendLine("	SELECT CASE Field");

            foreach (var fieldName in distinctFields)
            {
                sb.AppendLine($"		CASE \"{fieldName}\"");
                sb.AppendLine($"			RETURN ValidVal_{fieldName}();");
            }

            sb.AppendLine("		CASE ELSE"); 
            sb.AppendLine("\t\t\tMessage(\"GetValidVal \" ,\"GetValidVal Error \" + \" ^n Unknown field:\"+ Field, 16);\r\n\t\t\tRETURN \"\";");
            sb.AppendLine("	END SELECT");
            sb.AppendLine("END");
            sb.AppendLine();
            sb.AppendLine();
            sb.AppendLine();
            sb.AppendLine();


            sb.AppendLine(" INT FUNCTION VerifyVal(STRING tag,INT PhaseNum,STRING value,STRING tagType,STRING recName)");
            sb.AppendLine("	SELECT CASE tag");

            foreach (var fieldName in distinctFields)
            {
                sb.AppendLine($"		CASE \"{fieldName}\"");
                sb.AppendLine($"			RETURN Verify_{fieldName}(tag,PhaseNum,value,tagType,recName);");
            }

            sb.AppendLine("		CASE ELSE");
            sb.AppendLine("\t\t\tMessage(\"VerifyVal\" ,\"VerifyVal Error \" + \" ^n Unknown tag:\"+ tag, 16);\r\n\t\t\tRETURN -1;");
            sb.AppendLine("	END SELECT");
            sb.AppendLine("END");
            sb.AppendLine();

            //foreach (var fieldName in distinctFields)
            //{
            //    sb.AppendLine($"FUNCTION {fieldName}()");
            //    sb.AppendLine("	STRING strVal = \"1,2,3,4,5,6,7\";");
            //    sb.AppendLine("	RETURN strVal;");
            //    sb.AppendLine("END");
            //    sb.AppendLine();
            //}

            string directoryPath = Path.GetDirectoryName(outputFilePath);
            if (!string.IsNullOrEmpty(directoryPath))
            {
                Directory.CreateDirectory(directoryPath);
            }
            File.WriteAllText(outputFilePath, sb.ToString());



        }

        public static void AppendMissingFunctions_ValidVal(IEnumerable<string> distinctFields, string functionsFilePath)
        {
            var existingFunctions = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

            bool isNewFile = false;

            if (File.Exists(functionsFilePath))
            {
                var fileContent = File.ReadAllText(functionsFilePath);

                var regex = new Regex(@"STRING FUNCTION\s+ValidVal_(\w+)\s*\(.*?\)", RegexOptions.IgnoreCase);

                var matches = regex.Matches(fileContent);

                foreach (Match match in matches)
                {
                    if (match.Success && match.Groups.Count > 1)
                    {
                        existingFunctions.Add(match.Groups[1].Value);
                    }
                }
            }
            else
            {
                string directoryPath = Path.GetDirectoryName(functionsFilePath);
                if (!string.IsNullOrEmpty(directoryPath))
                {
                    Directory.CreateDirectory(directoryPath);
                }

                isNewFile = true;
            }

            var sb = new StringBuilder();

            if (isNewFile)
            {
                sb.AppendLine("//************************************************************");
                sb.AppendLine("//*  This file contains auto-generated functions             *");
                sb.AppendLine("//*  Please fill in each function with the                   *");
                sb.AppendLine("//*  appropriate return values.                              *");
                sb.AppendLine();
                sb.AppendLine("//*  These values will appear in dropdown menus              *");
                sb.AppendLine("//*  (ComboBoxes) for the end users.                         *");
                sb.AppendLine();
                sb.AppendLine("//*  Example:                                                *");
                sb.AppendLine("//*      FUNCTION rec_MyTag()                                *");
                sb.AppendLine("//*          STRING strVal = \"Option1,Option2,Option3\";      *");
                sb.AppendLine("//*          RETURN strVal;                                  *");
                sb.AppendLine("//*      END                                                 *");
                sb.AppendLine();
                sb.AppendLine("//*  Do NOT remove or rename any function.                   *");
                sb.AppendLine("//*  Only modify the return values as needed.                *");
                sb.AppendLine("//************************************************************");
                sb.AppendLine();

                sb.AppendLine();
            }

            foreach (var fieldName in distinctFields)
            {
                if (!existingFunctions.Contains(fieldName))
                {
                    sb.AppendLine($"STRING FUNCTION ValidVal_{fieldName}()");
                    sb.AppendLine("    STRING strVal = \"1,2,3,4,5\";//ADD VAL FOR COMBO");
                    sb.AppendLine("    RETURN strVal;");
                    sb.AppendLine("END");
                    sb.AppendLine();
                }
            }


            if (sb.Length > 0)
            {
                try
                {
                    if (isNewFile)
                    {
                        File.WriteAllText(functionsFilePath, sb.ToString());
                    }
                    else
                    {
                        File.AppendAllText(functionsFilePath, sb.ToString());
                    }
                }
                catch (IOException ioEx)
                {
                    Console.WriteLine($"I/O error while writing to file: {ioEx.Message}");
                }
                catch (UnauthorizedAccessException uaEx)
                {
                    Console.WriteLine($"Access denied when writing to file: {uaEx.Message}");
                }
                catch (Exception ex)
                {
                    Console.WriteLine($"Unexpected error while appending functions: {ex.Message}");
                }
            }
        }


