        public static void AppendMissingFunctions_Verify(IEnumerable<string> distinctFields, string functionsFilePath)
        {
            var existingFunctions = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

            bool isNewFile = false;

            if (File.Exists(functionsFilePath))
            {
                var fileContent = File.ReadAllText(functionsFilePath);

                var regex = new Regex(@"INT FUNCTION\s+Verify_(\w+)\s*\(.*?\)", RegexOptions.IgnoreCase);

                var matches = regex.Matches(fileContent);

                foreach (Match match in matches)
                {
                    if (match.Success && match.Groups.Count > 1)
                    {
                        existingFunctions.Add(match.Groups[1].Value);
                    }
                }
            }
            else
            {
                string directoryPath = Path.GetDirectoryName(functionsFilePath);
                if (!string.IsNullOrEmpty(directoryPath))
                {
                    Directory.CreateDirectory(directoryPath);
                }

                isNewFile = true;
            }

            var sb = new StringBuilder();

            if (isNewFile)
            {
                sb.AppendLine("//********************************************************************************");
                sb.AppendLine("//*  This file contains auto-generated validation functions                      *");
                sb.AppendLine("//*  Each function is responsible for verifying and saving                       *");
                sb.AppendLine("//*  recipe values entered by the end user.                                      *");
                sb.AppendLine("//*                                                                              *");
                sb.AppendLine("//*  These functions are called during recipe editing or                         *");
                sb.AppendLine("//*  execution, to validate and optionally store values.                         *");
                sb.AppendLine("//*                                                                              *");
                sb.AppendLine("//*  Function signature:                                                         *");
                sb.AppendLine("//*      INT FUNCTION Verify_rec_<TagName>(                                      *");
                sb.AppendLine("//*          STRING RName,                                                       *");
                sb.AppendLine("//*          INT PhaseNum,                                                       *");
                sb.AppendLine("//*          STRING tag,                                                         *");
                sb.AppendLine("//*          STRING value)                                                       *");
